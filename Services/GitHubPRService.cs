using Octokit;
using System.Text;

namespace K8SMonitor.Services;

public class GitHubPRService
{
    private readonly GitHubClient _client;
    private readonly string _owner;
    private readonly string _repo;
    private readonly string _baseBranch;

    public GitHubPRService(string token, string owner, string repo, string baseBranch = "main")
    {
        _client = new GitHubClient(new ProductHeaderValue("K8SMonitor"))
        {
            Credentials = new Credentials(token)
        };
        _owner = owner ?? throw new ArgumentNullException(nameof(owner));
        _repo = repo ?? throw new ArgumentNullException(nameof(repo));
        _baseBranch = baseBranch;
    }

    /// <summary>
    /// Creates a single PR with fixes for multiple files from a pod error.
    /// All fixes are applied to the same branch and included in one PR.
    /// </summary>
    public async Task<int> CreatePullRequestWithMultipleCodeFixesAsync(string branchName, string title, string description, List<CodeFix> codeFixes)
    {
        try
        {
            if (!codeFixes.Any())
                throw new InvalidOperationException("No code fixes provided");

            // Get the default branch SHA
            var mainRef = await _client.Git.Reference.Get(_owner, _repo, $"heads/{_baseBranch}");
            var mainSha = mainRef.Object.Sha;

            // Create new branch
            var newRef = new NewReference($"refs/heads/{branchName}", mainSha);
            await _client.Git.Reference.Create(_owner, _repo, newRef);

            Console.WriteLine($"✓ Created branch: {branchName}");

            // Apply all fixes to their respective files
            var appliedFixes = new List<CodeFix>();
            foreach (var codeFix in codeFixes)
            {
                // Fetch existing file from GitHub
                var existingFile = await GetFileContentAsync(codeFix.FilePath);
                
                if (existingFile == null)
                {
                    Console.WriteLine($"  ⚠️  Could not find file: {codeFix.FilePath} - skipping this fix");
                    continue;
                }

                // Apply the fix to the code
                var fixedContent = existingFile.Content.Replace(codeFix.OriginalCode, codeFix.FixedCode);
                
                if (fixedContent == existingFile.Content)
                {
                    Console.WriteLine($"  ⚠️  Code fix could not be applied to {codeFix.FilePath} - original code not found");
                    continue;
                }

                // Update file with fixed code
                var updateRequest = new UpdateFileRequest(
                    message: $"Auto-fix: {title}",
                    content: fixedContent,
                    sha: existingFile.Sha,
                    branch: branchName
                );
                
                await _client.Repository.Content.UpdateFile(_owner, _repo, codeFix.FilePath, updateRequest);
                Console.WriteLine($"  ✓ Fixed file: {codeFix.FilePath}");
                appliedFixes.Add(codeFix);
            }

            if (!appliedFixes.Any())
            {
                throw new InvalidOperationException("No fixes could be successfully applied to any files");
            }

            // Build PR body with all fixes
            var changesSection = new StringBuilder();
            changesSection.AppendLine("## 🔧 Changes Made\n");
            
            foreach (var fix in appliedFixes)
            {
                changesSection.AppendLine($"### 📄 {fix.FilePath}");
                changesSection.AppendLine($"**Why:** {fix.Explanation}\n");
                changesSection.AppendLine("#### Original Code");
                changesSection.AppendLine($"```csharp\n{fix.OriginalCode}\n```\n");
                changesSection.AppendLine("#### Fixed Code");
                changesSection.AppendLine($"```csharp\n{fix.FixedCode}\n```\n");
                changesSection.AppendLine("---\n");
            }

            var prBody = $@"{description}

{changesSection}

**Files Modified:** {appliedFixes.Count}
- {string.Join("\n- ", appliedFixes.Select(f => f.FilePath))}

---

**Automated by K8SMonitor** - This PR was automatically generated by analyzing Kubernetes pod logs and using AI to suggest and apply fixes to multiple files.";

            var prRequest = new NewPullRequest(title, branchName, _baseBranch)
            {
                Body = prBody
            };

            var pr = await _client.PullRequest.Create(_owner, _repo, prRequest);
            Console.WriteLine($"✓ Created Pull Request #{pr.Number} with {appliedFixes.Count} file fix(es): {pr.HtmlUrl}");

            return pr.Number;
        }
        catch (Exception ex)
        {
            throw new InvalidOperationException($"Failed to create pull request with multiple code fixes: {ex.Message}", ex);
        }
    }

