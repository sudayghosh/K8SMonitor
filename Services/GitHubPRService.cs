    public class FileContent
    {
        public string Content { get; set; } = string.Empty;
        public string Sha { get; set; } = string.Empty;
        /// <summary>Resolved full path in the repository (e.g. src/Worker/Program.cs)</summary>
        public string Path { get; set; } = string.Empty;
    }

    public async Task<FileContent?> GetFileContentAsync(string filePath)
    {
        try
        {
            var content = await _client.Repository.Content.GetAllContentsByRef(_owner, _repo, filePath, _baseBranch);
            var file = content.FirstOrDefault();

            if (file == null)
                return null;

            string decodedContent;
            try
            {
                // GitHub wraps base64 with newlines every 60 chars — strip all whitespace before decoding
                var cleanBase64 = file.Content
                    .Replace("\n", "")
                    .Replace("\r", "")
                    .Replace(" ", "");
                decodedContent = System.Text.Encoding.UTF8.GetString(
                    Convert.FromBase64String(cleanBase64)
                );
            }
            catch (FormatException)
            {
                // Octokit may have already decoded the content in some versions/configurations
                decodedContent = file.Content ?? string.Empty;
            }

            return new FileContent
            {
                Content = decodedContent,
                Sha = file.Sha,
                Path = filePath
            };
        }
        catch (Exception ex)
        {
            Console.WriteLine($"  ⚠️  GetFileContentAsync failed for '{filePath}': {ex.Message}");
            return null;
        }
    }

    /// <summary>
    /// Searches the entire repo tree for a file matching the given filename (e.g. "Program.cs").
    /// Returns the first full path found (e.g. "src/Worker/Program.cs"), or null if not found.
    /// </summary>
    public async Task<string?> SearchFileByNameAsync(string fileName)
    {
        try
        {
            var tree = await _client.Git.Tree.GetRecursive(_owner, _repo, _baseBranch);
            var match = tree.Tree.FirstOrDefault(item =>
                item.Type == TreeType.Blob &&
                string.Equals(System.IO.Path.GetFileName(item.Path), fileName, StringComparison.OrdinalIgnoreCase));
            return match?.Path;
        }
        catch
        {
            return null;
        }
    }

    public async Task<int> CreatePullRequestAsync(string branchName, string title, string description, string fileName, string fileContent)
    {
        try
        {
            // Get the default branch SHA
            var mainRef = await _client.Git.Reference.Get(_owner, _repo, $"heads/{_baseBranch}");
            var mainSha = mainRef.Object.Sha;

            // Create new branch
            var newRef = new NewReference($"refs/heads/{branchName}", mainSha);
            await _client.Git.Reference.Create(_owner, _repo, newRef);

            Console.WriteLine($"✓ Created branch: {branchName}");

            // Create or update markdown file with solutions
            var existingFile = await GetFileIfExistsAsync(fileName);
            
            if (existingFile != null)
            {
                var updateRequest = new UpdateFileRequest(
                    message: $"Auto-fix: {title}",
                    content: Convert.ToBase64String(System.Text.Encoding.UTF8.GetBytes(fileContent)),
                    sha: existingFile.Sha,
                    branch: branchName
                );
                await _client.Repository.Content.UpdateFile(_owner, _repo, existingFile.Path, updateRequest);
                Console.WriteLine($"✓ Updated file: {fileName}");
            }
            else
            {
                var createRequest = new CreateFileRequest(
                    message: $"Auto-fix: {title}",
                    content: Convert.ToBase64String(System.Text.Encoding.UTF8.GetBytes(fileContent)),
                    branch: branchName
                );
                await _client.Repository.Content.CreateFile(_owner, _repo, fileName, createRequest);
                Console.WriteLine($"✓ Created file: {fileName}");
            }

            // Create pull request
            var prRequest = new NewPullRequest(title, branchName, _baseBranch)
            {
                Body = $"{description}\n\n**Automated by K8SMonitor**\n\nThis PR was automatically generated by analyzing Kubernetes pod logs and using AI to suggest fixes.\n\n**Fix Details:** See the `{fileName}` file in this PR for complete analysis and solutions."
            };

            var pr = await _client.PullRequest.Create(_owner, _repo, prRequest);
            Console.WriteLine($"✓ Created Pull Request #{pr.Number}: {pr.HtmlUrl}");

            return pr.Number;
        }
        catch (Exception ex)
        {
            throw new InvalidOperationException($"Failed to create pull request: {ex.Message}", ex);
        }
    }

    private async Task<RepositoryContent?> GetFileIfExistsAsync(string filePath)
    {
        try
        {
            var content = await _client.Repository.Content.GetAllContentsByRef(_owner, _repo, filePath, _baseBranch);
            return content.FirstOrDefault();
        }
        catch
        {
            return null;
        }
    }

